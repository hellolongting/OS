package file.tableView;

public class Table {
	private int index;
	private String used;
	public Table(int index,String used) {
		this.index=index;
		this.used=used;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getUsed() {
		return used;
	}
	public void setUsed(String used) {
		this.used = used;
	}
	
	
}

